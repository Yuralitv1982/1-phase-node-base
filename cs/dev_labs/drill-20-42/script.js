// 1. Принимаем наш сырой байт от датчика (0xAD = 1010 1101 в двоичной)
const payload = 0xad;

console.warn(
  `Сырой байт от датчика: ${payload.toString(2).padStart(8, '0')} (hex: ${payload.toString(16)})\n`,
);

// ==========================================
// ЗАДАЧА 1: Узнать статус датчика (самый левый бит)
// ==========================================
// Используем "Битововую маску" с помощью оператора И (&).
// Правило &: 1 & 1 = 1, все остальные комбинации = 0.
// Наша маска: 1000 0000 (в hex это 0x80). Мы "светим" только на первый бит.
const statusMask = 0x80;
const isOnline = (payload & statusMask) !== 0; // Если результат не 0, значит бит был 1

console.warn(
  `1. Маска статуса (0x80):  ${statusMask.toString(2).padStart(8, '0')}`,
);
console.warn(
  `Результат (payload & statusMask): ${(payload & statusMask).toString(2).padStart(8, '0')}`,
);
console.warn(`Датчик онлайн? => ${isOnline}\n`);

// ==========================================
// ЗАДАЧА 2: Вытащить тип тревоги (4 правых бита)
// ==========================================
// Нам нужны последние 4 бита. Делаем маску 0000 1111 (в hex это 0x0F)
const alarmMask = 0x0f;
const alarmType = payload & alarmMask;

console.warn(
  `2. Маска тревоги (0x0F):  ${alarmMask.toString(2).padStart(8, '0')}`,
);
console.warn(
  `Тип тревоги (число от 0 до 15): ${alarmType} (в бинарном: ${alarmType.toString(2).padStart(4, '0')})\n`,
);

// ==========================================
// ЗАДАЧА 3: Вытащить приоритет (3 бита в середине: 4, 5, 6)
// ==========================================
// Здесь нам нужен битовый сдвиг ВПРАВО (>>).
// Нам мешают 4 правых бита тревоги. Давайте сдвинем всё число на 4 позиции вправо!
// 10101101 >> 4 превратится в 00001010
let priority = payload >> 4;

// Теперь приоритет стоит справа, но у нас слева остался бит статуса!
// Нам нужны только 3 бита (маска 0000 0111, hex 0x07)
const priorityMask = 0x07;
priority = priority & priorityMask;

console.warn(
  `3. Сдвиг вправо на 4 (payload >> 4): ${(payload >> 4).toString(2).padStart(8, '0')}`,
);
console.warn(
  `Наложили маску (0x07):                 ${priorityMask.toString(2).padStart(8, '0')}`,
);
console.warn(
  `Уровень приоритета: ${priority} (в бинарном: ${priority.toString(2).padStart(3, '0')})`,
);
