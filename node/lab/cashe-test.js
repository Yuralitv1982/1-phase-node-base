const { performance } = require('perf_hooks');

// Создаем матрицу 10 000 на 10 000.
// Это 100 миллионов элементов.
// Каждый элемент Int32 занимает 4 байта. Итого: ~400 Мегабайт памяти.
// Это гарантированно вывалится за пределы L1/L2/L3 кэша процессора (обычно они до 32 Мб).
const ROWS = 10000;
const COLS = 10000;

console.log(`Выделяем ${Math.round((ROWS * COLS * 4) / 1024 / 1024)} МБ физической памяти...`);
const matrix = new Int32Array(ROWS * COLS);

// =======================================================
// ТЕСТ 1: Идеальная локальность (Обход по строкам)
// =======================================================
console.log('\nЗапуск Теста 1: Обход по строкам (Cache-friendly)...');
const startRow = performance.now();

// Идем горизонтально: Строка 1 (слева направо), потом Строка 2...
for (let r = 0; r < ROWS; r++) {
    for (let c = 0; c < COLS; c++) {
        // Формула вычисления индекса 1D массива как 2D матрицы
        matrix[r * COLS + c] = 1;
    }
}

const endRow = performance.now();
const timeRow = endRow - startRow;
console.log(`Время (строки): ${timeRow.toFixed(2)} мс`);


// =======================================================
// ТЕСТ 2: Ужасная локальность (Обход по столбцам)
// =======================================================
console.log('\nЗапуск Теста 2: Обход по столбцам (Cache-killer)...');
const startCol = performance.now();

// Идем вертикально: Столбец 1 (сверху вниз), потом Столбец 2...
// Обрати внимание: мы просто поменяли циклы местами!
for (let c = 0; c < COLS; c++) {
    for (let r = 0; r < ROWS; r++) {
        matrix[r * COLS + c] = 2;
    }
}

const endCol = performance.now();
const timeCol = endCol - startCol;
console.log(`Время (столбцы): ${timeCol.toFixed(2)} мс`);

// =======================================================
// РЕЗУЛЬТАТЫ
// =======================================================
console.log('\n--- ИТОГ ---');
console.log(`Разница в скорости: в ${(timeCol / timeRow).toFixed(1)} раз!`);
if (timeCol > timeRow) {
    console.log('Вывод: Неправильный обход убивает кэш процессора и заставляет его ждать оперативную память.');
}
